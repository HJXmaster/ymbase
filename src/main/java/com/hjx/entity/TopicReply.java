package com.hjx.entity;

public class TopicReply {
	private Integer trId;
	private Integer newsId;
	private String trUid;
	private Integer trReplyId;
	private String trTime;
	private String trContext;
	private Integer trSupport;
	private String username;
	private String title;
	public Integer getTrId() {
		return trId;
	}
	public void setTrId(Integer trId) {
		this.trId = trId;
	}
	public Integer getNewsId() {
		return newsId;
	}
	public void setNewsId(Integer newsId) {
		this.newsId = newsId;
	}
	public String getTrUid() {
		return trUid;
	}
	public void setTrUid(String trUid) {
		this.trUid = trUid;
	}
	public Integer getTrReplyId() {
		return trReplyId;
	}
	public void setTrReplyId(Integer trReplyId) {
		this.trReplyId = trReplyId;
	}
	public String getTrTime() {
		return trTime;
	}
	public void setTrTime(String trTime) {
		this.trTime = trTime;
	}
	public String getTrContext() {
		return trContext;
	}
	public void setTrContext(String trContext) {
		this.trContext = trContext;
	}
	public Integer getTrSupport() {
		return trSupport;
	}
	public void setTrSupport(Integer trSupport) {
		this.trSupport = trSupport;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}

}
